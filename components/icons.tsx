export const BetTypeIcon: React.FC<{ betType: string; className?: string }> = ({ betType, className = 'h-6 w-6' }) => {
  const lowerCaseBet = betType.toLowerCase();
  
  if (lowerCaseBet.includes('over')) return <ArrowTrendingUpIcon className={className} />;
  if (lowerCaseBet.includes('under')) return <ArrowTrendingDownIcon className={className} />;
  
  switch (betType) {
    case 'Home Win':
    case 'Away Win':
    case 'Draw No Bet':
      return <TrophyIcon className={className} />;
    case 'Draw':
      return <HandshakeIcon className={className} />;
    case 'BTTS (Yes)':
      return <TeamsIcon className={className} />;
    case 'Double Chance':
      return <DoubleChanceIcon className={className} />;
    case 'Win to Nil':
      return <ShieldIcon className={className} />;
    case 'Correct Score':
        return <ScoreboardIcon className={className} />;
    case 'Asian Handicap':
        return <ScaleIcon className={className} />;
    case 'Handicap':
        return <PlusMinusIcon className={className} />;
    case 'Half-Time Result':
        return <ClockIcon className={className} />;
    default:
      return <TrophyIcon className={className} />;
  }
};

